<script type="text/javascript">

	$(document).ready(function(){
		$('#submit').click(function(){
			var nome = $('#nome').val();
            var email = $('#email').val();
			var fone = $('#fone').val();
			var whatsapp = $('#whatsapp').val();
            var msg = $('#msg').val();

			$('#alert').html('');
			if (nome == '') {
				$('#alert').html('Preencher o nome.');
				$('#alert').addClass("alert-danger");
				return false;				
			}

            $('#alert').html('');
			if (email == '') {
				$('#alert').html('Preencher o email.');
				$('#alert').addClass("alert-danger");
				return false;				
			}

			$('#alert').html('');
			if (fone == '') {
				$('#alert').html('Preencher o telefone.');
				$('#alert').addClass("alert-danger");
				return false;
			}

			$('#alert').html('');
			if (whatsapp == '') {
				$('#alert').html('Preencher a whatsapp.');
				$('#alert').addClass("alert-danger");
				return false;
			}

            $('#alert').html('');
			if (msg == '') {
				$('#alert').html('Preencher a mensagem.');
				$('#alert').addClass("alert-danger");
				return false;
			}

			$('#alert').html('');

			$.ajax({
				url:'./controller/enviar.php',
				method: 'POST',
				data: {nome: nome, email:email, fone:fone, whatsapp:whatsapp, msg:msg},
				success: function(result) {
					$('form').trigger("reset");
					$('#alert').addClass("alert-success");
					$('#alert').fadeIn().html(result);
					setTimeout(function(){
						$('#alert').fadeOut('Slow');
					},3000);
				}
			});


		});
	});

</script>